function downwardVerticalCollision(x,y) {
    var logicalX = Math.floor(x.pos / 25);
    var logicalX2 = Math.ceil(x.pos / 25);
    var logicalY = 12 - Math.floor(y.pos / 50);
    
    if (map[logicalY][logicalX] == 1
        || map[logicalY][logicalX2] == 1
        || map[logicalY][logicalX2+1] == 1
        || map[logicalY][logicalX2+2] == 1
        || map[logicalY][logicalX2+3] == 1) {
        return true;
    }
    return false;
}

Controller.prototype.update = function() {
    var object = this.character;
    var x = object.vector.x;
    var y = object.vector.y;


    var maxSpeed = object.maxSpeed;
    if (this.gamePad.right()) x.acc = 1500;
    else if (this.gamePad.left()) x.acc = -1500;
    else if (x.speed != 0) { x.acc = -1500 * Math.sign(x.speed); maxSpeed = 0;}
    else x.acc = 0;
    if (this.gamePad.up() && object.grounded) y.speed = 1000;


    updateComponent(x, x.acc, maxSpeed);
    console.log(x.speed);

    if (x.pos > this.hLimit) {
        x.pos = this.hLimit;
        x.speed = 0;
    } else if (x.pos < 0) {
        x.pos = 0;
        x.speed = 0;
    } else {
        h = 12 - Math.floor(y.pos / 50);

        if (Math.sign(x.speed) == 1 && map[h][Math.floor(x.pos / 25 + 4)] == 1) {
            x.pos = Math.floor(x.pos / 25) * 25;
            x.speed = 0;
        }
        if (Math.sign(x.speed) == -1 && map[h][Math.floor(x.pos / 25)] == 1) {
            x.pos = Math.floor(x.pos / 25 + 1) * 25;
            x.speed = 0;
        }
    }

    updateComponent(y, -2500, 750);
    object.grounded = false;
    if (Math.sign(y.speed) == -1 && downwardVerticalCollision(x,y)) {
        y.pos = Math.floor(y.pos / 50 + 1) * 50;
        y.speed = 0;
        object.grounded = true;
    }

    object.update();



    if (this.mousePad.fire() || this.gamePad.fire()) {
        this.bullets.push(new Bullet(this.character.vector.x.pos+100, this.character.vector.y.pos - 35, 1000));
    }

    for (var i = this.bullets.length; i-- > 0; ) {
        updateComponent(this.bullets[i].vector.x, 0, 1000);
        if (this.bullets[i].vector.x.pos > 2000) {
            this.bullets[i].remove();
            this.bullets.splice(i, 1);
        } else {
            this.bullets[i].update();
        }
    };
}

